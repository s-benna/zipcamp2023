package day05_wrapperClassConcatenationMantikOperatorleri;

public class C02_Concatenation {
    public static void main(String[] args) {

        // Concatenation: Yan yana yazdirma. Java String bir data gordugunde icinde sayi da olsa fotograf olarak
        // kabul eder ve araya koyulan + islemi degil yan yana yazdirma islemi yaptirir.


        System.out.println(3+5);            //8
        System.out.println("Ali"+"Can");    //AliCan
        System.out.println("3"+"5");        //35
        System.out.println("A"+5);          //A5
        System.out.println(3+5+"B");        //8B
        System.out.println("C"+3+5);        //C35
        System.out.println("C"+(3+5));      //C8
        System.out.println("D"+3*5);        //C15











    }
}
